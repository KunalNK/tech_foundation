#Function to add two numbers  
def add(num1, num2):
    return num1 + num2

#Function to substract two numbers 
def sub(num1,num2):
    return num1-num2

#Function to multiply two numbers
def mul(num1,num2):
    return num1*num2

#Function to divide two numbers
def div(num1,num2):
    return num1/num2


